// --- Public API ------------------------------------------------------

export function buildPositioningPrompt(
  input: PositioningInput
): PositioningPromptMessages {
  return {
    system: POSITIONING_SYSTEM_PROMPT,
    user: buildUserMessage(input),
  }
}
